----------------------------------------------------------------------------------------------------------------------
# Create a New User:

sudo adduser nameuser

----------------------------------------------------------------------------------------------------------------------
# Enable Password Authentication:

sudo su -
nano /etc/ssh/sshd_config
passwordAuthentication = yes
service sshd restart

----------------------------------------------------------------------------------------------------------------------
# Delete a User:

sudo deluser nameuser

----------------------------------------------------------------------------------------------------------------------
# Get all User's:

less /etc/passwd

----------------------------------------------------------------------------------------------------------------------
# Add New User for Sudo:

sudo visudo
nameuser ALL=(ALL:ALL) ALL

----------------------------------------------------------------------------------------------------------------------
# Alter Password for User:

sudo passwd root

